<h2 id="paragraph">1. Portal (2007)</h2>
    <img src="img/portal.jpg" alt="Portal" class="jatekimg">
<div class="harmasoszlop"> <p>A Portalnem más, mint a Half-Life világába ültetett ügyességi játék harmatos akcióelemekkel megtűzdelve.
    <b>Harcolni nem tudunk, csupán egy speciális térfakasztó berendezéssel két térkaput nyithatunk a pálya majdnem
    bármely részén  legyen az talaj, a fal vagy a mennyezet azon szegletei melyek nem fémből vannak. </b>Azért ez így
    nem is hangzik olyan borzasztóan, ahogyan a megvalósítás sem az. A Portal fegyver használata pofonegyszerű:
    bal egér gombbal a kék, a jobb kattintással pedig a kapu piros végét nyithatjuk meg, így ha besétálunk az
    egyik ajtón, akkor a másikon pottyanunk ki és fordítva. A gravitáció komoly hatással van a kapuk helyzetére,
    ugyanis amilyen lendülettel érkezünk be a portálba, olyan lendülettel fogunk kijönni a másik végén, amit
    rengetegszer használnunk is kell majd a játék során. Remekül oldották meg a betanítás folyamatát, ugyanis
    a történet szerint amúgy is egy kísérleti alanyt játszunk - de erről később -, akinek egyre jobban el kell
    sajátítania a fegyver adta lehetőségeket. Kezdetben a szerkentyű képességei is csököttek, hiszen sokáig csak
    a térfolyosó egyik kijáratát mozgathatjuk  a másik már egy adott ponton meg van nyitva és így kell felmászni,
    bemászni, átmászni a szint végét jelentő liftig. Mindemellett a térgörbítést használnunk kell bizonyos
    tárgyak áthelyezéséhez is; legtöbbször a nehezéknek számító dobozokat , a híres-neves Companion Cube-okat
    kell megmozgatnunk, illetve a Half-Life 2-ből ismert pattogó energiagömböket kell áttessékelni a megfelelő
    ívben egy bizonyos pontba, hogy ennek hatására kinyíljon egy ajtó, ami persze újabb kihívások elé állít.
    Tehát ezzel a spéci masina segítségével kell megoldanunk különböző ügyességi feladatokat, hogy elérjük az
    adott szint végét és indulhassunk a következőre. Összesen 19 szinten kell végigsakkozni magunkat, melyek
    közül az első tíz nem túlzottan jelent kihívást, ám aztán fokozatosan összetettebb feladatok érkeznek,
    míg a végére már néha az agyunkat is meg kell erőltetni egy-egy megoldás megálmodásához.
</p>
</div>
<h2  class="paragraph"> 2. Bioshock (2007)</h2>
    <img src="img/bioshock.jpg" alt="Bioshock" class="jatekimg">
<p>A Bioshock már az első pillanatban elvarázsolja az embert. <b>A lezuhant repülőgépből a partra úszva azonnal
    Rapture mérnöki pontossággal megtervezett világában találjuk magunkat.</b> Az art deco stílusban életre keltett
    metropolisz minden egyes épülete, tárgya az utolsó pixelig hiteles marad. Semmi sem esik le a képről vagy
    tűnik idegennek a világban. A neonfények, a falakon található hirdetések, a kezelőfelület, a fegyverek és
    a plasmoidok megvalósítása éppoly tökéletes, mint a használatukat bemutató súgóablak, ami az 50-es évek
    reklámjaiból visszaköszönő képkockákkal magyarázza el működésüket. Rapture tehát él és hiteles. A
    játéktörténelem valaha készült legszebb és talán leghangulatosabb világa, nem beszélve arról, hogy
    művészeti szempontból is új etalonként üdvözül a számítógépes játékok palettáján.
</p>
<p>
    A képi világ azonban nem lenne elég ahhoz, hogy igazi klasszikusról beszéljünk. A Bioshock szerencsére
    képes túlnőni az eddig megismert FPS-eken - <mark>a játékban ugyanis szinte minden úgy történik, ahogy Te akarod.</mark>
    A lineárisan ívelő történetmesélés lassan kibomlik, a szokásos harcok egyre több taktikát és stratégiát
    igényelnek, a plazmoidok száma folyamatosan bővül, csakúgy, mint az arzenálunk. Minden egyes fegyverhez
    háromféle töltény szerezhető, vagy bizonyos gépeken legyártható, így ha kedvünk van, a hagyományos shotgun
    lőszert lecserélhetjük robbanó vagy elektromos töltetre. Pár óra játék után teljesen kinyílik a világ, s
    bár vannak korlátai, mégis azt az érzetet kelti, hogy minden küzdelem végkimenetele és a történet alakulása,
    befolyásolása csak rajtunk múlik. Hihetetlen érzés. Addiktív és okos, olyan, amilyennel csak ritkán
    találkozik az ember.
</p>
<h2  class="paragraph"> 3. Fallout 3 (2008)</h2>
    <img src="img/fallout.jpg" alt="Fallout 3" class="jatekimg">
<p> A Fallout sosem volt bombasiker, hatalmas aranybánya, de a <mark>végletekig kidolgozott szerepjáték-rendszerével,
    körökre osztott, taktikus harcával, a 2D izometrikus, hihetetlen hangulatot sugárzó grafikájával és
    dermesztő taktusaival örökre beírta magát a videojátékok nagykönyvébe</mark>. Az első rész hamar az egykori
    Interplay egyik legpatinásabb címe lett, a folytatás pedig az egyik lehető legbugosabb játékként, de
    egyben az egyik legjobb folytatásként vonult be a köztudatba. A lehengerlő szerepjátékos duót egy közepes
    akcióprogram (Fallout Tactics) követte, majd az Interplay csődjével beállt a csend, csak a masszív
    rajongótábor harmadik részért áhítozók fohászai zúgtak halkan a háttérben...</p>
<p>
    Pletykák évekig keringtek az internet bugyraiban, aztán 2007 áprilisában robbant a bomba: a Bethesda öt
    és fél millió dollár ellenében megszerezte a franchise jogait és azonnal nekiállt a folytatás készítésének.
    Radioaktív skorpiókon edződött vérpistikék ezrei ragadtak rögvest billentyűzetet, h
    ogy tiltakozzanak a
    Morrowind mutánsokkal bővített változata ellen, de ez nem hatotta meg a fejlesztőket, nyáron már az
    első teaser csalt libabőrt karjainkra és először mertük hangosan megkockáztatni: itt valami rendkívüli készül.
</p>
<h2  class="paragraph"> 4. Shadow of the Colossus (2005)</h2>
    <img src="img/shadow.png" alt="Shadow of the colossus" class="jatekimg">
<p> A Shadow of the Colossus, Japánban Wander and the Colossus, egy akció-kalandjáték, amit az SCE Japan
    Studio és a Team ICO jelentetett meg és a Sony Computer Entertainment adott ki Playstation 2-re. A játék
    Japánban és Észak-Amerikában 2005 októberében, a többi régióban 2006 februárjában jelent meg.
</p>
<p>
    A játék lényege egyszerű, <b>meg kell keresni a kolosszusokat</b>, és meg kell ölni őket. Minden egyes alkalommal
    amikor egy kolosszus meghal, Wander, a főszereplő, visszatér a központi részere, hogy megismételje a
    folyamatot. Ahhoz, hogy megtaláljunk egy-egy ellenséget, a főhősnek fel kell emelnie a kardját, amin a
    napfény megmutatja az adott kolosszushoz vezető utat. A legtöbb egy elzárt helyen található, mint például
    barlangok mélyén, tavakban vagy egy ősi épület romjai között. Amint megtalálta a játékos, meg kell keresni
    annak a gyengeségeit, ahhoz, hogy le lehessen győzni. Minden egyes kolosszust máshogy kell legyőzni, és
    néhánynál használni kell a környezetet is. Minden kolosszusnak van legalább egy gyenge pontja, amit egy
    fénylő mágikus szimbólum jelez. Mindegyiknek vannak olyan részei ami szőrrel van borítva, vagy platformok
    állnak ki belőlük amin Wander meg tud kapaszkodni miközben az megpróbálja lerázni magáról. A játékban vagy
    egy mérő ami folyamatosan csökken amikor Wander kapaszkodik vagy mászik, és amint ez elfogy Wander leesik.
    Wandernek és a kolosszusoknak is van életerő csíkja. A kolosszusok életereje jelentősen csökken amikor a
    gyenge pontjait támadja a játékos, Wander pedig megsérülhet az óriások támadásaitól vagy az eséstől. A
    játékban Wander egy karddal és egy íjjal rendelkezik. Ugyan a játékban csak a kolosszusok az ellenségek,
    megtalálhatóak még állatok is, amiből csak egy fajtának van jelentősége a játékban. Egy bizonyos fajta gyík
    farkának elfogyasztása növeli Wander állóképességét. Ugyan így, vannak bizonyos gyümölcsök, amik növelik az
    életerőt. Wander lova, Agro fontos szerepet játszik a játékban. Lóháton kell bejárni a területeket és néhány
    kolosszusnál a harc is lóháton történik, viszont vannak olyan területek, ahova nem lehet Agroval bemenni. A
    játék angol verziójában Agro hím ló, azonban Ueda Fumito nőstényként tekint a lóra.
</p>


<h2  class="paragraph"> 5. Resident Evil 4 (2005)</h2>
    <img src="img/resident.png" alt="Resident Evil 4" class="jatekimg">
<p> <mark>A történet érdekes és csavaros, ráadásul igen jó stílusban és hangulatban mesélik el. A szinkronhangok
    remekek, a kamera ügyesen idomul </mark>a (sajnos igen igénytelen módon tömörített) videókhoz, a vágások
    dinamizmusa példaértékű; egy szó, mint száz, a Resident Evil 4 tökéletes alapot nyújt egy jó kis
    túlélő horrornak. Az, hogy menet közben egyre inkább úgy érezzük, valamiféle digitális regényben
    szerepelünk, ne lepjen meg: a sodró lendületet a Capcomnál dinamikus kamerával és külső (3rd person)
    nézettel oldották meg, a zombik mellett elhagyva a sorozat másik védjegyét, a rögzített nézetet, és a
    harmadikat, a kissé lassúcska játékmenetet is.
</p>

<h2  class="paragraph">1. Half-Life</h2>
    <img src="img/halflife.jpg" alt="Half-life" class="jatekimg">
<p>A ’90-es évek FPS programjairól nem igazán állíthatjuk azt, hogy összetett, komolyan vehető történeteket
    meséltek volna el. A 2000-es évekre már megszaporodott a történet hajtotta akciójátékok populációja,
    és ez nem kis mértékben a Half-Life-nak volt köszönhető. A játéknak, mely mind történetvezetés, mind
    játékmenet terén komoly változtatásokat hozott az akkoriban még csak gyerekcipőben totyogó, frissen
    született stílusba. Cikkemben azt szeretném kifejteni, hogy miért is lehetett annyira meghatározó a
    Half-Life a maga idejében.
</p>
<p> Főhősünk a Black Mesa Kutatóintézet egyik alkalmazottja, a 27 éves Gordon Freeman, aki a játék kezdetén
    egy teleportációs kísérlet félresikerült eredményeként kaput nyit az úgynevezett Határvilágra, a Xenre,
    ahonnan agresszív idegen létformák kezdenek átözönleni a titkos létesítménybe. A kormány hamar tudomást
    szerez az általa üzemeltetett intézményben történt katasztrófáról, és azonnal kirendeli a helyszínre az
    ilyen különleges esetekre felkészített katonai alakulatait. A tehetetlen tudósok arra várnak, hogy a
    katonaság megmentse őket, míg Freeman feszítővasat és lőfegyvert ragad, és a saját kezébe veszi a helyzetet
    irányítását.
</p>
<p> A készítők a játékmenetet rendelték alá a történetnek, és nem fordítva; miközben kényesen ügyeltek a két
    említett összetevő közötti egyensúly megtartására. A hitelességhez való ragaszkodás sohasem megy a
    játékmenet rovására: a harcokat nem fogták csak azért vissza, mert egy nem túlzottan harcedzett tudóst
    irányítunk (sőt, erre a történet még épít is a folytatásokban). Egyúttal olyan játékelemek sem kerültek
    elő, amik a játék által felállított történeti szabályok hitelességérzetét tennék semmisé. Így (a korai
    FPS-ekel szemben) itt nincsenek ok nélkül, megmagyarázhatatlan módon lebegő páncélok, különleges lőszerek
    és erőnövelők a pályákon. Minden játékelemnek megvan a racionális magyarázata, logikája és helye a játék
    világán belül, és nem csak egyszerű játékmechanikai célokat szolgálnak.
</p>
<p> És ha már játékmenetnél tartunk: felfegyverzett tudósunk komoly arzenállal bír. Az ikonikussá vált pajszeren,
    és a félresikerült kísérlet után rajta maradt (a viselő aktuális egészségügyi állapotáról szóban tájékoztató)
    különleges, gyakorlatilag katonai célokra is alkalmas védőruhán kívül egyéb játékszerekkel is biztosíthatjuk
    a főhős védelmét. Lesz sörétesünk, pisztolyunk, rakétavetőnk, gépkarabélyunk, gránátunk, és egyéb idegen
    eredetű, vagy épp prototípus állapotban leledző fegyverünk is. Ezek közül szinte mindegyik hasznos,
    és működésükre sem lehet panasz; lesz is mire használnunk őket.
</p>
<p> Az ellenfelek javát harcias idegenek fogják kitenni, változatos termettel, adottságokkal, képességekkel
    és harcmodorral. Az útonállók másik felét pedig a profi célzó és gránáthajító képességgel büszkélkedő
    katonaság alkotja. Így nem lesz hiány kemény harcokban, amiket a rátermett (bár olykor megbotló) MI még
    inkább meg fog nehezíteni. A pályák felépítése pedig ügyesen váltogatja a harcokhoz ideálisan kialakított
    terepeket, és a (Doom-okkal ellentétben itt nem szó szerint értendő) továbbjutás kulcsát gondosan elrejtő,
    gondolkodásra sarkalló pályarészeket, amik legtöbbször a történetmesélés szerves részét is képezik.
</p>
<p> Ezenkívül a Half-life sokadik (maga idejében) különleges(nek számító) eleme az is, hogy utunk során nem
    csak ellenséges karakterekkel fogunk összeakadni. A Black Mesa őreivel és tudósaival is találkozunk,
    akik néha egy-egy mondatban fontos információkat osztanak meg velünk a történettel kapcsolatban, de
    egyéb hasznuk is van. Az őrök és tudósok „felszólításra” követnek minket, és kinyithatnak nekünk olyan
    utunkba eső ajtókat, amikhez csak nekik van hozzáférésük, és amelyek mögött értékes ellátmányok rejlenek.
    Az őrök továbbá fegyvereikkel is védenek, a dokik pedig kritikus helyzetekben gyógyítanak minket. Komoly
    személyiséggel viszont egyikük sem rendelkezik, inkább csak egyéniség nélküli archetípusok. A folytatásokban
    ez a két karaktertípus egy-egy, valóban egyedi jellemben született újjá (Dr. Isaac Kleiner és Barney
    Calhoun személyében).
</p> <br>
<h2  class="paragraph">2. Final Fantasy VII.  </h2>
    <img src="img/finafantasy.jpg" alt="Final Fantasy VII." class="jatekimg">
<p> A Final Fantasy VII akkor született, amikor a Square Enix (akkoriban még Squaresoft) a legkreatívabb
    korszakában volt – mind ami a játékmenetet, a történetet, vagy épp a technológiai megoldásokat illeti.
    Annak idején elképzelhetetlen volt az, amit a Square az FF7-tel megvalósított: a háromdimenziós csaták,
    a remek harci animációk, a renderelt karakterek, és a játékmenettel szervesen összefolyó, filmszerű
    átvezetők egyszerűen lenyűgözték a játékosokat és a kritikusokat egyaránt. Ha mindehhez hozzávesszük,
    hogy a Square lapot húzott a tizenkilencre, és a jól ismert és szeretett FF világát először helyezte
    disztopikus sci-fi környezetbe, a szerteágazó, minden eddiginél kidolgozott történet, valamint komplexebb
    karakterek pedig azonnal elrabolták a játékosok szívét, nem csoda, hogy az FF7 a sajtóban szinte kizárólag
    maximum pontos kritikákat kapott. A sorozat és a Square legkedveltebb, legtöbb példányban eladott játékává
    vált - és maradt a mai napig. <br> A rajongók hosszú évek óta könyörögtek a Square-nek, hogy újítsa fel a
    játékot, hiszen a mellékszálak; a Final Fantasy VII: Advent Children, a Crisis Core és a Dirge of Cerberus
    bizony igen felemásra sikeredtek.
</p>
<h2  class="paragraph">3. DOOM/DOOM II: Hell on Earth </h2>
    <img src="img/doom.jpg" alt="doom" class="jatekimg">
<p> Az id Software 1993-ban a Doommal műfajt, legendát, forradalmat hozott. A titok nyitja ma már eléggé
    sablonos és épp, hogy elfogadható, de akkoriban a rengeteg platform- és kalandjáték (illetve RPG)
    után merőben más és merész húzás volt, s az újdonság varázsával aratott. A recept egyszerű volt:
    lőjj szét mindent, ami mozog, keresd meg a kulcsot, és lécelj le a pályáról! Mégis a változatos
    ellenfelek, a jól összerakott pályák, és az öteletes fegyverek hatalmasat ütöttek 1993-ban. Így
    egyértelmű volt: ezt folytatniuk kell! Az id így 1994-ben kihozta a 2. részt Doom 2: Hell on Earth címmel.
</p>
<h2  class="paragraph"> 4. Metal Gear Solid</h2>
    <img src="img/metalgear.jpg" alt="Metal Gear Solid" class="jatekimg">
<p>  1987-ben jelent meg az alkotás, melyet az akkor szakmai berkekben még ismeretlen japán játéktervező
    és író, Kodzsima Hideo talált ki.
</p>
<p> Az első önálló projektje, amely kiadásra is került, 1987-ben került a polcokra Metal Gear néven. A kor
    akciójátékaitól eltérően nem az volt a cél, hogy mindenkit elpusztítsunk, aki szembe jön a főhőssel,
    éppen ellenkezőleg, lehetőleg el kellett kerülni a konfrontációt, ajánlatos volt inkább kicselezni az
    ellenséget. Kodzsima vezetésével elkészült az első „stealth action”, vagy ahogy a jó magyar ember mondja,
    az első „lopakodós” játék. A program főhőse Solid Snake volt, aki a FOXHOUND nevű alakulat újoncaként
    került bevetésre egy afrikai országban, hogy elpusztítson egy újfajta tömegpusztító fegyvert, melynek
    később a Metal Gear nevet adták. A kor játékaihoz képest kidolgozott történettel és újszerű játékmenettel
    rendelkező játék nagy siker lett, aminek eredményeképpen számos platformra átportolták. 1990-ben Kodzsima
    Hideo vezetésével elkészült a folytatás is (Metal Gear 2: Solid Snake), majd 1998-ban megjelent a Metal
    Gear Solid, meghozva a nemzetközi sikert és elismerést a japán fejlesztőnek.
</p>
<p>
    A játék először a Sony Playstation platformjára jelent meg, és csak két évvel később tette tiszteletét
    számítógépünkön. A két verzió között eget verő különbségek nem voltak, persze PC-n javult a grafika, a
    felbontást is feljebb tornázhattuk, de a játékmenet nem változott. A PC-s változatba bekerült a Virtual
    Mission nevű csomag, amiben egy virtuális világban egyre bonyolultabb küldetéseket oldhattunk meg. A
    főhős maradt a régi, tehát Solid Snake bőrébe kell bújnunk, aki már nem a FOXHOUND egység tagja:
    visszavonultan él az alaszkai tundrán egészen addig, amíg egy nap fegyveres katonák jelennek meg nála,
    akik „megkérik”, hogy fáradjon el régi barátjához, Roy Campbell ezredeshez. A játék menüjéből elérhető
    eligazításból (Briefing) megtudhatjuk, hogy egy terrorista csapat, amely Snake régi egységének tagjaiból
    verbuválódott, elfoglalt egy katonai bázist Shadow Moses szigetén, túszul ejtve több fontos személyt is.
    Ha még ez nem lenne elég, a bázis genetikailag felturbózott katonákból álló állománya egy felkelést
    követően a rosszfiúk oldalára állt, így Snake-nek gyakorlatilag egy hadsereggel kell egyedül szembenéznie.
    A terroristák követelése — egy rakás pénzen kívül —, hogy az Egyesült Államok kormánya adja át nekik a
    valaha élt legnagyobb harcos, Big Boss genetikai maradványait. Meglehet furcsa egy kérés, ám a történet
    során minden értelmet nyer.
</p>
<h2  class="paragraph"> 5. Super Metroid</h2>
    <img src="img/supermetroid.jpg" alt="Super Metroid" class="jatekimg">
<p> A játék a kutatásra összpontosít, a játékos olyan erőforrásokat keres, amelyeket korábban elérhetetlen
    területek elérésére használnak. Új fogalmakat tartalmaz a sorozatban, például a leltár képernyőt, az
    automatikus térképet és a tüzet minden irányba.
</p>
<p>
    A Super Metroid egy 2D - s , oldalra görgethető akció-kaland játék , amely elsősorban az eredeti játék
    kitalált Zebes bolygóján játszódik - egy nagy, nyitott világban, ajtókkal és liftekkel összekapcsolt
    területekkel. A játékos ellenőrzése Samus Aran , ahogy keresi a bolygó egy Metroid , hogy ellopták a
    Ridley , a vezető a Space Pirates . Samus nyolc irányban képes futni, ugrani, guggolni és fegyvert
    lőni; más műveleteket is végezhet, például falugrást - gyors egymásutánban az egyik falról a másikra
    ugrálva , hogy magasabb területekre jusson. A "Moon Walk" képesség, amelyet az azonos nevű népszerű
    táncmozgásról neveztek el , lehetővé teszi Samus számára, hogy hátralépjen, miközben fegyvert lő vagy
    tölt. A játékos a játék folyamán olyan erőforrásokat szerezhet, amelyek fokozzák Samus páncélját és
    fegyvereit, valamint különleges képességeket biztosítanak számára, lehetővé téve számukra, hogy
    hozzáférjenek olyan területekhez, amelyekhez korábban nem lehetett hozzáférni. A Morphing Ball lehetővé
    teszi, hogy Samus gömbölyödjön és szűk helyekre guruljon; míg ebben a formában bombákat ültethet, ha egy
    bombát bekapcsolnak. A Tavaszi Bál hozzáadja az ugrás képességét, miközben Morphing Ball formában van.
    A Speed Booster használható nagy sebességgel történő futáshoz, és akadályokba és ellenségekbe ütközhet.
    A Hi-Jump Boots lehetővé teszi a magasabb ugrást, a Space Jump pedig Samus számára, hogy a levegőbe
    ugorjon. A markológerenda a nyílt területeken történő lengéshez használható. A röntgensugár segítségével
    tárgyakat és járatokat láthatunk a rejtett falakon és egyéb felületeken keresztül.
</p>

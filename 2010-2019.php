<h2  class="paragraph">2. The legend of Zelda: Breath of the wild</h2>
    <img src="img/zelda.jpg" alt="The legend of Zelda" class="jatekimg">
<p>Elképesztően merész és vakmerő döntés volt a világ egyik legkreatívabb fejlesztőcsapatától, hogy a több
    mint 30 éves múltra visszatekintő, szakmai sikereket halmozó szériájukat szinte a nulláról újraépítsék.
    A Breath of the Wild ugyanis papíron mindent sutba dobva hatalmas magabiztossággal a nyitott világú játékok
    megreformálását tűzte ki célul úgy, hogy közben a sorozat összes védjegyét, kreativitását és ismérvét
    megtartva megtisztelje azokat a rajongókat is akik évek, vagy évtizedek óta kitartanak a széria mellett.
</p>
<p>
    A Breath of the Wild legfontosabb eleme a realizmus. Végre megszületett egy olyan nyitott világú játék,
    amiben minden a valós fizika törvényszerűségeinek engedelmeskedik, ahol minden mindennel kapcsolatban van,
    amiben csakúgy, mint a való életben, logikus gondolkodással szinte bármi megvalósítható. Ez a valós
    környezet azonban csak úgy tud észrevehetően működni, ha a készítők magára hagyják a játékosokat, akik
    csak a kitűzött célról szereznek tudomást, de az odavezető út - a mára sajnos trenddé váló megoldásoktól
    eltérően - nem szkriptek sorozatán keresztül vezető téglákkal van kikövezve. Jó példa erre az első küldetés,
    ahol el kell jutni a havasok közt található mini dungeon bejáratához. Más játékkal szemben a BotW nem segít,
    nem fogja a kezedet, hanem hagyja, hogy saját tapasztalataidon keresztül te alakítsd a sikerig vezető utat.
</p>
<p>
    A realizmus és a fizika tehát kihat mindenre, így a Zelda ámulatba ejtő módon folyamatosan meghökkentő
    pillanatokat okoz. Ha harcolunk, a felénk rohanó ellenfelek kezéből kiüthetjük a fegyvert, ami a lejtős
    terepen legurul. A lény ekkor felkapja a legközelebbi harci eszközt, és ha mást nem talál, hát egy
    kővel ront ránk, amit felénk hajítva próbál megsebezni minket. Igen ám, de fegyverünket baseball
    ütőként használva simán visszaüthetjük a felénk hajított kavicsot fejen koppintva így a támadót.
    Ha fabunkója a tűzbe gurul és lángra kap a nyele, a goblin felkapva azt megégeti magát és sérül, de
    simán előfordulhat az is, hogy tüzes nyílvesszőnktől lángra kapó pajzsát öngyújtóként használva maga
    lobbantja lángra fabunkóját, majd hatalmas hahotázás közben a nagyobb sebzés reményében azzal ront ránk.
    Ha valaki elektromos fegyverrel talál el minket, akkor az áramütéstől kiejtjük a kezünkből az összes fém
    tárgyat, amiket jó esetben simán összeszedhetünk, de előfordulhat, hogy szomorúan kell konstatáljuk,
    nehezen szerzett fegyverünk a hegy oldalán megpattanva a mélybe hullik. Persze ilyenkor azonnal beugrik,
    hogy fa fegyverre kell váltani, hiszen az nem vezeti az áramot!
</p>
<h2  class="paragraph">3. Spelunky</h2>
    <img src="img/spelunky.jpg" alt="Spelunky" class="jatekimg">
<p>A szabályok spártaian egyszerűek. Minden pályán meg kell találnod a kivezető ajtónyílást, ami valahol
    lent, az ismeretlen mélységben vár. Pár élettel, bombával és kötélhágcsóval kezded leereszkedésed az
    alsóbb szintekre, s mindenfajta segítség nélkül kell kitapasztalnod a csapdák és gonosz lények működésének
    mikéntjét. Utad során denevérek, bumerángos őslakosok és hasonlók támadnak rád, de vagyonod az elszórt
    drágaköveket, kincset rejtő ládákat felszedve szépen gyarapodik, sőt, fegyvereket és barlangászathoz
    hasznos eszközöket is beszerezhetsz.
</p>
<p>
    A halál után az egész játékot az elejéről kell kezdened. A legelejéről. Négy pályára, pályánkénti
    négy szakaszra osztva kell átvergődnöd a változatos arculatú barlangokon, és az utolsó etapon is
    elbukhatsz. Persze nem fogsz eljutni odáig, mert (és ezzel tutira megfogom a kihívásokra éhes
    platformrajongókat) annyira nehéz a játék, hogy vért izzadó kőszoborhoz hasonló csoda az, amikor nem
    purcansz ki rögtön.
</p>
<h2  class="paragraph">4. Kentucky Route Zero </h2>
    <img src="img/kentucky.jpg" alt="Kentucky Route Zero" class="jatekimg">
<p>A Kentucky Route Zero az alapjait tekintve egy faék-egyszerűségű point & click játék. Egyedül az
    egerünk bal gombjára van szükségünk az összes cselekvéshez, amit a játékban végrehajtunk: a
    közlekedéshez, a felfedezéshez, a párbeszéd-darabok kiválasztásához. Az időnk nagy részét különböző
    helyszíneken töltjük, ahol néhány tárgyat megvizsgálhatunk, szóba elegyedhetünk a jelenlévőkkel,
    miközben szép lassan haladunk előre az egyre inkább „szétterülő” történettel.
</p>
<p>
    A játék eleji szállítmányozás csupán kiindulópont. Ahányszor vissza akarunk térni ehhez a feladathoz,
    annyiszor fognak minket elterelni az események. Egyszer egy bányában ragadunk Shanonn-nal, máskor egy
    orvost keresünk az erdő mélyén, vagy éppen Junebug kocsmai fellépését hallgatjuk. A történések
    jönnek-mennek, mi pedig sodródunk az eseményekkel. A Kentucky Route Zero arra épít, hogy nem az úti cél
    a fontos, hanem az út, amit megteszünk addig.
</p>
<p>
    Maga a sztori lineáris: kis különbségektől eltekintve mindig ugyanaz történik az elején és a végén is.
    Hogy mi akkor benne az interaktív? A szereplők. Mikor átkattintgatod a párbeszédeket, és kiválasztod a
    neked tetsző opciókat, karaktereket építesz. Senki nem rendelkezik fix háttértörténettel, csupán nagy
    vonalakban ismered meg azt – a te dolgod, hogy a vonalak közötti rést megtöltsd. Milyen kapcsolatban volt
    Shannon és az unokatestvére? Mi történt pontosan Conway főnökének gyermekével? Számtalan kérdés
    megválaszolása csakis rád vár – már ha egyáltalán megválaszolod. Megteheted, hogy csak hallgatsz, a
    való életben sem nyílik meg mindenki az első idegennek.
</p>
<h2  class="paragraph">5. Pokémon Go</h2>
    <img src="img/pokemongo.jpg" alt="Poekémon Go" class="jatekimg">
<p>Ötvözi az augmented reality élménnyel, illetve a legendás cím semmihez se fogható hangulatával.
    A Pokémon Go egy mobilalkalmazás, mely egy felfedeznivalókkal teli vadonná változtatja a városunkat,
    össze kell gyűjteni, majd edzeni és harcba vezetni a pokémonokat. A játékmenet a valóság és a virtuális
    világ összekötésére épít, ki kell mozdulnunk otthonról, sétálni és bejárni a várost, a játék egy
    átszínezett, átrajzolt Google-térkép segítségével mutatja aktuális helyzetünket, az alkalmazás pedig
    riaszt, ha pokémont érzékel a közelben. Fel kell kutatnunk a kis rejtőzködőt, majd indulhat a befogás,
    de ugyanitt látjuk a környéken fellelhető Pokéstopokat, illetve Gymeket is.
</p>
<p>
    Ha riaszt az alkalmazás, akkor valahol mellettünk már meg is jelenik egy pokémon. Rá kell nyomni,
    mire bekapcsolódik a telefonunk kamerája, mi pedig meglátjuk őt, ahogy ott ül József Attila szobra
    mellett, vagy épp nekünk készül ugrani egy BKK-automata tetejéről. Elkapásához meg kell dobni őt
    pokélabdával, valahogy úgy, ahogy az előző játékokban vagy rajzfilmen is láttuk, a feladat viszont
    nem mindig egyszerű. Sokszor kivédi a dobást, elugrik előle, vagy ha el is kaptuk, akkor kiszabadul
    és elszalad, vannak példányok, akiket elsőre el tudunk rakni, és olyanok is, akiket több tucat labdával,
    többször is meg kell támadnunk. Ha megfogtunk egy pokémont, akkor ő onnantól a miénk, bekerül a Pokédexbe,
    ahol adatokat, fotót és részletes leírást nézhetünk meg róla, illetve elérhető is lesz, mint bevethető harcos.
</p>
<p>
    <i>A Pokéstopok különféle nevezetességek a városban.</i> Meglátogatásukkal pokéballokat és itemeket szerezhetünk
    be, minden második sarkon van legalább egy belőlük. Oda kell sétálni, és már meg is jelenik a telefonunk
    képernyőjén egy érme, rajta egy fotóval a helyről. Az érmét oldalra húzással pörgethetjük meg, pár
    másodperccel később pedig már potyognak is ki belőle az ajándékok. A pokéballok mellett kaphatunk
    életelixírt, mely a legyőzött harcosainkat támasztja fel, illetve gyógyszert a sérült pokémonok
    meggyógyításához, de ugyanitt szerezhető be a pokécsali is, amit aktiválva fél óráig magunkhoz vonzhatjuk
    a környék pokémonjait. A csalival meg lehet többszörözni az elkapható mennyiséget.
</p>


